test_that("fit simple AR model works", {
  testY <- c(1.838, 0.382, -0.306, 2.185, 0.458, -2.366, -0.959, -1.014, -0.158, -2.133)
  testX <- c(1:10)
  testNow <- c(1,2,3)
  test_mod <- fit_ar(testY, X_nowcast = testNow, params = list(p = 2))
  expect_equal(test_mod[[1]]$coef[[1]], 0.2423, tolerance = 0.001)
  expect_equal(test_mod[[1]]$coef[[2]], -0.1160, tolerance = 0.001)
  expect_equal(test_mod[[1]]$coef[[3]], -0.2140, tolerance = 0.001)
  expect_equal(test_mod$prediction$prediction[1], -0.6855, tolerance = 0.001)
  expect_equal(test_mod$prediction$prediction[2], -0.1056, tolerance = 0.001)
  expect_equal(test_mod$prediction$prediction[3], -0.1330, tolerance = 0.001)
})